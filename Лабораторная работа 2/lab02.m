X = [0.3, 0.7, 1.3, 1.4, 1.9, 2, 2.5, 3.1, 4];
Y = [-2, -5, -7, -8, -11, -12, -16, -17, -25];

%Кол-во параметров (два массива)
n = 2;

%Свободные члены
B = [sum(Y);
    sum(X.*Y);];

%Матрица 2*2 по 
A = [n, sum(X);
    sum(X), sum(X.^2);];

% coef = inv(A)*B; равнозначно
coef = A\B;

y = @(X) coef(1)+coef(2).*X;
plot(X, Y);
hold on;
plot(X, y(X));

legend ('Ломанная', 'ŷ = -0.0254269813855303 -5.98541546728075 * x');